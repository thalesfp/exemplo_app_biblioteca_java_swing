    public void completarCampos(int posicao) {
        posicaoPedido = posicao;
        
        Pedido pedido = controllerPedidos.todos().get(posicao);
        
        cbClientes.setSelectedItem(pedido.getCliente());
        
        DecimalFormat df = new DecimalFormat("0.00");
        
        for (ItemPedido itemPedido : pedido.getItensPedidos()) {
            Object ob [] = {
                itemPedido.getLivro(), 
                itemPedido.getQuantidade(),
                itemPedido.getLivro().getPreco(),
                itemPedido.getLivro().getPreco() * itemPedido.getQuantidade()
            };
            itemPedidoModel.addRow(ob);
        }
        
        if(pedido.getDataEmissao()!=null) {
            DateFormat dfo = new SimpleDateFormat("d/M/yy", Locale.ENGLISH);
            this.tfDataEmissao.setText(dfo.format(pedido.getDataEmissao()));
        }
        
        lValorTotal.setText(Float.toString(pedido.getValorTotal()));
        cbFormaPagamento.setSelectedItem(pedido.getFormaPagamento());
        cbSituacao.setSelected(pedido.isSituacao());
    }

