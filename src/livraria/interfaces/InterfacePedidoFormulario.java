    private void btRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btRemoverActionPerformed
        int position = tableItemPedido.getSelectedRow();
        
        if (position>=0) {
            float valorTotal = Float.parseFloat(tableItemPedido.getValueAt(position, 3).toString());
            float valorTotalAtual = Float.parseFloat(lValorTotal.getText());
            
            DecimalFormat df = new DecimalFormat("0.00");
            
            lValorTotal.setText(df.format(valorTotalAtual - valorTotal));
            
            itemPedidoModel.removeRow(position);
        }
    }//GEN-LAST:event_btRemoverActionPerformed

