    private void btAdicionarItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAdicionarItemActionPerformed
        Livro livroSelecionado = controllerLivros.todos().get(cbProduto.getSelectedIndex()); 
        Float valorItem = livroSelecionado.getPreco();
       
        DecimalFormat df = new DecimalFormat("0.00");
        
        Object ob [] = {livroSelecionado, sQuantidade.getValue(), livroSelecionado.getPreco(), df.format(valorItem * (Integer) sQuantidade.getValue())};
        itemPedidoModel.addRow(ob);
        
        Float valorTotalAtual = Float.parseFloat(lValorTotal.getText());
        Float total = valorTotalAtual + (valorItem * (Integer) sQuantidade.getValue());
        
        lValorTotal.setText(df.format(total));
        
        sQuantidade.setValue(1);
    }//GEN-LAST:event_btAdicionarItemActionPerformed

    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
 
        Pedido novoPedido = new Pedido();
        
        List<ItemPedido> listItensPedidos = new ArrayList();
        
        for(int i = 0 ; i<tableItemPedido.getRowCount() ; i++) {
            ItemPedido itemPedido = new ItemPedido();
            itemPedido.setLivro((Livro) tableItemPedido.getValueAt(i, 0));
            itemPedido.setQuantidade(Integer.parseInt(tableItemPedido.getValueAt(i, 1).toString()));
            itemPedido.setPreco(Float.parseFloat(tableItemPedido.getValueAt(i, 2).toString()));
            
            listItensPedidos.add(itemPedido);
        }
        
        novoPedido.setItensPedidos(listItensPedidos);

        int ultimoNumero = 1;
        
        if (controllerPedidos.todos().size()>0) {
            ultimoNumero = controllerPedidos.todos().get(controllerPedidos.todos().size()-1).getNumero() + 1;
        }
        
        try {
            Date date = new SimpleDateFormat("d/M/yy", Locale.ENGLISH).parse(tfDataEmissao.getText());
            novoPedido.setDataEmissao(date);
        } catch (ParseException e) {
            
        }

        novoPedido.setValorTotal(Float.parseFloat(lValorTotal.getText()));
        novoPedido.setFormaPagamento(cbFormaPagamento.getSelectedItem().toString());
        novoPedido.setSituacao(cbSituacao.isSelected());
        
        Cliente cliente = (Cliente) cbClientes.getSelectedItem();
        novoPedido.setCliente(cliente);
        
        Endereco endereco = new Endereco();
        
        endereco.setBairro(cliente.getBairro());
        endereco.setCEP(cliente.getCEP());
        endereco.setCidade(cliente.getCidade());
        endereco.setComplemento(cliente.getComplemento());
        endereco.setEstado(cliente.getEstado());
        endereco.setNumero(cliente.getNumero());
        endereco.setRua(cliente.getRua());
        
        novoPedido.setEntrega(endereco);
        
        try {
            Date date = new SimpleDateFormat("d/M/yy", Locale.ENGLISH).parse(tfDataEmissao.getText());
            novoPedido.setDataEmissao(date);
        } catch (ParseException e) {
            
        }
        
        if (posicaoPedido==null) {
            novoPedido.setNumero(ultimoNumero);
            controllerPedidos.salvar(novoPedido);
        } else {
            controllerPedidos.atualizar(posicaoPedido, novoPedido);            
        }
        
        btCancelarActionPerformed(evt);
    }//GEN-LAST:event_btSalvarActionPerformed

