    public InterfacePedido() {
        initComponents();
        this.setLocationRelativeTo(null);
        
        pedidos = controllerLivros.todos();
        
        model = new DefaultTableModel();
        model.addColumn("Número");
        model.addColumn("Data Emissão");
        model.addColumn("Valor Total");
        model.addColumn("Entrega");
        
        for (Pedido pedido : pedidos) {
            Object ob []= {
                pedido.getNumero(),
                new SimpleDateFormat("d/M/yy", Locale.ENGLISH).format(pedido.getDataEmissao()),
                pedido.getValorTotal(),
                pedido.getEntrega()
                };
            model.addRow(ob); 
        }
        
        tablePedidos.setModel(model);
    }

