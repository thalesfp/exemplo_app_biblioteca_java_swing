    private void btRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btRemoverActionPerformed
        int position = tablePedidos.getSelectedRow();
        
        if (position>=0) {
            Pedido pedido = controllerLivros.todos().get(position);
            controllerLivros.remover(pedido);
            model.removeRow(position);
        }
        
    }//GEN-LAST:event_btRemoverActionPerformed

    private void btEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEditarActionPerformed
        int position = tablePedidos.getSelectedRow();
        
        if (position >= 0) {
            InterfacePedidoFormulario interfacePedidoFormulario = new InterfacePedidoFormulario();
            interfacePedidoFormulario.completarCampos(position);
            interfacePedidoFormulario.setVisible(true);
            dispose();
        }
    }//GEN-LAST:event_btEditarActionPerformed

