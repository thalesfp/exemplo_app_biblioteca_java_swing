    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
       Cliente novoCliente = new Cliente();

       novoCliente.setNome(tfNome.getText());
       novoCliente.setEmail(tfEmail.getText());
       novoCliente.setFone(tfFone.getText());
       
       novoCliente.setLogin(tfLogin.getText());
       novoCliente.setSenha(pfSenha.getText());
       
       novoCliente.setRua(tfRua.getText());
       
       if(!tfNumero.getText().equals(""))
            novoCliente.setNumero(Integer.parseInt(tfNumero.getText()));
       
       novoCliente.setComplemento(tfComplemento.getText());
       
       if(!tfCep.getText().equals(""))
            novoCliente.setCEP(Integer.parseInt(tfCep.getText()));
       
       novoCliente.setBairro(tfBairro.getText());
       novoCliente.setCidade(tfCidade.getText());
       novoCliente.setEstado(cbEstado.getSelectedItem().toString());
       
       if (posicaoCliente==null) {
           controllerClientes.salvar(novoCliente);
       } else {
           controllerClientes.atualizar(posicaoCliente, novoCliente);           
       }
       
        btCancelarActionPerformed(evt);
    }//GEN-LAST:event_btSalvarActionPerformed

