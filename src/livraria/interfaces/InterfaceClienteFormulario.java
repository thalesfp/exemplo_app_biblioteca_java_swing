    public void completarCampos(int posicao) {
        posicaoCliente = posicao;
        
        Cliente cliente = controllerClientes.todos().get(posicao);
        
        this.tfNome.setText(cliente.getNome());
        this.tfEmail.setText(cliente.getEmail());
        this.tfFone.setText(cliente.getFone());
        
        this.tfLogin.setText(cliente.getLogin());
        this.pfSenha.setText(cliente.getSenha());
        
        this.tfRua.setText(cliente.getRua());
        this.tfNumero.setText(Integer.toString(cliente.getNumero()));
        this.tfComplemento.setText(cliente.getComplemento());
        this.tfCep.setText(Integer.toString(cliente.getCEP()));
        this.tfBairro.setText(cliente.getBairro());
        this.tfCidade.setText(cliente.getCidade());
        this.cbEstado.setSelectedItem(cliente.getEstado());
    }
    
